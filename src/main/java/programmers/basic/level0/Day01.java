package programmers.basic.level0;

/**
 * 사칙연산
 */
public class Day01 {

    /**
     * 두수의 합
     */
    public int plusSolution(int num1, int num2) {
        return num1 + num2;
    }

    /**
     * 두수의 차
     */
    public int minusSolution(int num1, int num2) {
        return num1 - num2;
    }

    /**
     * 두수의 곱
     */
    public int multiplySolution(int num1, int num2) {
        return num1 * num2;
    }

    /**
     * 몫 구하기
     */
    public int divideSolution(int num1, int num2) {
        return num1 / num2;
    }
}
